class OneFiftyThree
{
	public static void main(String args[])
	{
		int back=6;
		int j=0;
		for(int i=0;i<=6;i++)
		{
			for(j=0;j<back;j++)
				System.out.print(" ");
				back--;
				
			for(j=0;j<i;j++)
									
				System.out.print("*");
				System.out.print("\n");
		}
		
	
	}


}