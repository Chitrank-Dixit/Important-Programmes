/*Replacing Old Character from new character*/
class Replace
{
	public static void main(String args[])
	{
		String s1="Sunny Day";
		System.out.println("before replace: "+s1);
		String s2=s1.replace("Sunny Day","Rainy Day");
		System.out.println("After replace: "+s2);
	
	
	
	
	}




}