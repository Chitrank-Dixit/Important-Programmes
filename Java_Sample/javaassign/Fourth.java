/*
Write an application that declares 5 integers, calculates and print the average of these numbers
*/
class Fourth
{
	public static void main(String args[])
	{
		int a,b,c,d,e;
		a=12;
		b=46;
		c=69;
		d=65;
		e=33;
		int avg=(a+b+c+d+e)/5;
		System.out.println("The average of the five integers is: "+avg);
	
	}

}