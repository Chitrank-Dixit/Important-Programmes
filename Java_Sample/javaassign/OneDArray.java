/*Declaring One dimenstional array */
class OneDArray
{
	public static void main(String args[]) throws Exception
	{
		int ara[]={200,400,120,450,234};
		for(int i=0;i<ara.length;i++)
		{
			System.out.println("ara ["+i+"] is "+ara[i]);
		}
		
		char ar[]=new char[8];
		ar[0]='c';
		ar[1]='h';
		ar[2]='i';
		ar[3]='t';
		ar[4]='r';
		ar[5]='a';
		ar[6]='n';
		ar[7]='k';
		
		for(int i=0;i<=ar.length;i++)
		{
			System.out.print(""+ar[i]);
		}
		
	
	}


}