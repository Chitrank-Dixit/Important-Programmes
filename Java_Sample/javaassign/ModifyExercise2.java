/* Modify Exercise 2 to force all the lines in the results to upper case and send the results to System.out */
class ModifyExercise2
{
	public static void main(String args[])throws Exception
	{
		for(int i=0;i<=args.length;i++)
		{
			String s=args[i].toUpperCase();
			System.out.print(" "+s);
		}
	
	
	
	
	}


}